package com.mj.bcalendar.fragment;

import android.os.Bundle;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ListView;
import androidx.fragment.app.ListFragment;
import com.mj.bcalendar.adapter.ScheduleListViewAdapter;

public class ScheduleFragment extends ListFragment {

    ScheduleListViewAdapter adapter;

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // 스케줄 화면 초기화
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState) {
        adapter = new ScheduleListViewAdapter();

        setListAdapter(adapter);
        createAdapter(adapter);

        return super.onCreateView(inflater, container, savedInstanceState);
    }

    @Override
    public void onListItemClick(ListView l, View v, int position, long id) {
        super.onListItemClick(l, v, position, id);

        ScheduleContentFragment contentDig = new ScheduleContentFragment();
        contentDig.show(getFragmentManager(),"test");
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // ** 스케줄 화면 초기화 마지막 부분
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // 스케줄 화면 메소드 모음
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    private void createAdapter(ScheduleListViewAdapter adapter){
        for(int i = 0; i  < 24; i++){
            String hour = "";
            int temp = i + 1;
            if(temp < 10){
                hour = "0" + temp + ":00";
            } else {
                hour = temp + ":00";
            }

             adapter.addItem(hour, "Test");
        }
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // ** 스케줄 화면 메소드 모음 마지막
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
